export const translateHK = (word) => {
  if (!word) return null;
  // Bỏ tiếng trung quốc nếu không có trong danh sách
  return HIEPKY_DICT[word] || null; 
};

export const getHiepKyInfo = (lunar) => {
  if (!lunar) return { cat: [], hung: [], nen: [], kieng: [] };
  
  const filterEmpty = (arr) => arr.filter(Boolean);
  
  const cat = filterEmpty(lunar.getDayJiShen().map(translateHK));
  const hung = filterEmpty(lunar.getDayXiongSha().map(translateHK));
  const nen = filterEmpty(lunar.getDayYi().map(translateHK));
  const kieng = filterEmpty(lunar.getDayJi().map(translateHK));
  
  return { cat, hung, nen, kieng };
};

